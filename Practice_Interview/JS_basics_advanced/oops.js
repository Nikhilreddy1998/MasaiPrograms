// **Abstraction**
// Showing only essential features of an object and hiding the internal implementation details. In JavaScript, this is often achieved using functions, classes, or interfaces (patterns) that expose only what is necessary.

// **Encapsulation**
// Binding data and methods together in a single unit (object/class) and restricting direct access to some components, usually using closures or private class fields.

// **Inheritance**
// A mechanism where one class/object acquires properties and methods from another class/object, promoting code reuse. In JavaScript, this is implemented using `extends` or prototype chaining.

// **Polymorphism**
// The ability of different objects to respond to the same method name in different ways, depending on their implementation.

